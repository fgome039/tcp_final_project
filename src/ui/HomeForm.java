    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JDialog fr = null;
		int tabSelectIndex = jTabbedPane1.getSelectedIndex();
        
        switch (tabSelectIndex) {
            case 0:
                Account newAccount = new Account(accountManager.getNextId(), 
						null, null, null, null, null, null, null, null, null);
                fr = new AccountForm(newAccount, accountManager, true);
                break;
            case 1: 
                Employee newEmployee = new Employee(employeeManager.getNextId(), 
						null, null, null, null, null);
                fr = new EmployeeForm(newEmployee, employeeManager, true);
                break;
            case 2:
                Material material = new Material(materialManager.getNextId(),
						null, SubstrateType.NONE, Color.WHITE, 0, 0, 0);
                fr = new MaterialForm(material, materialManager, true);
                break;
			default:
                fr = new WorkOrderForm(new WorkOrder(workOrderManager.getNextId(), 
						1, new Date(), Machine.MANUAL, WorkMethod.LAMINATION, 1, 
						0, 0, 1, 1, null), 
					accountManager, employeeManager, materialManager, 
					workOrderManager, true);
                break;
        }
		
		fr.addWindowListener(new WindowAdapter() {
			@Override
			public void windowClosed(WindowEvent e) {
				refresh();
			}
		});
		fr.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
		JDialog fr = null;
		int selection;
		int tabSelectIndex = jTabbedPane1.getSelectedIndex();
		
		switch (tabSelectIndex) {
            case 0:
                selection = accountsTable.getSelectedRow();
				if (selection != -1)
					fr = new AccountForm(accountManager.get(selection),
						accountManager, false);
				 
				break;
            case 1: 
				selection = employeesTable.getSelectedRow();
				if (selection != -1)
					fr = new EmployeeForm(employeeManager.get(selection),
						employeeManager, false);
				
                break;
            case 2:
				selection = materialsTable.getSelectedRow();
				if (selection != -1)
					fr = new MaterialForm(materialManager.get(selection),
						materialManager, false);
                break;
			default:
				selection = workOrdersTable.getSelectedRow();
				if (selection != -1)
					fr = new WorkOrderForm(workOrderManager.get(selection), 
						accountManager, employeeManager, materialManager, 
						workOrderManager, false);
                break;
        }
		
		if (fr != null) {
			fr.addWindowListener(new WindowAdapter() {
				@Override
				public void windowClosed(WindowEvent e) {
					refresh();
				}
			});
			fr.setVisible(true);
		}
    }//GEN-LAST:event_jButton3ActionPerformed

